    @staticmethod
    def _safe_int(x, default=0.0):
        try:
            if x is None:
                return default
            s = str(x).replace(",", "").strip()  # ‡∏ï‡∏±‡∏î comma
            return float(s)
        except Exception:
                return default

    @staticmethod
    def _normalize_name(s: str) -> str:
        """‡∏ï‡∏±‡∏î‡∏Ñ‡∏≥‡∏ô‡∏≥‡∏´‡∏ô‡πâ‡∏≤ ‡πÅ‡∏•‡∏∞‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏≠‡∏≠‡∏Å"""
        if not s:
            return ""
        s = s.strip()
        # ‡∏ï‡∏±‡∏î‡∏Ñ‡∏≥‡∏ô‡∏≥‡∏´‡∏ô‡πâ‡∏≤
        for title in ("‡∏ô‡∏≤‡∏¢", "‡∏ô‡∏≤‡∏á‡∏™‡∏≤‡∏ß", "‡∏ô‡∏≤‡∏á"):
            if s.startswith(title):
                s = s[len(title):].strip()
                break
        # ‡πÄ‡∏≠‡∏≤‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏≠‡∏≠‡∏Å‡∏´‡∏°‡∏î
        s = s.replace(" ", "")
        return s

    @staticmethod
    def _similar(a: str, b: str) -> float:
        """‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤‡∏Ñ‡∏ß‡∏≤‡∏°‡πÄ‡∏´‡∏°‡∏∑‡∏≠‡∏ô (0.0 - 1.0)"""
        a = a or ""
        b = b or ""
        return SequenceMatcher(None, a, b).ratio()

